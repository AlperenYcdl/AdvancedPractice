package practiceAdvanced.practice03;

public class Q02_Ascii_AdiniYaz {
    public static void main(String[] args) {


        // Adinizin icerdigi harfleri kullanmadan adinizi yazdiran bir kod yaziniz.

        char ch1= 'B'-1; // A
        char ch2= 'm'-1; // l
        char ch3= 'h'+1; // i

        System.out.println(""+ch1+ch2+ch3);


        char ad1= 'C'-1;
        char ad2= 'v'-1;
        char ad3= 't'-1;
        char ad4= 's'-1;
        char ad5= 'b'-1;
        System.out.println(""+ad1+ad2+ad3+ad4+ad5);







    }
}
